    <h2>What actually drives the price</h2>

    <p>Price in web design is primarily a function of four variables: scope, customisation, who you hire, and how much strategic thinking is included in the process.</p>

    <span class="section-label">Scope</span>
    <p>More pages, more features, more integrations — more cost. A five-page business website with a contact form is a very different project from a fifteen-page site with a blog, a booking system, a CRM integration, and a multilingual setup. Both are "a website." Neither costs the same.</p>

    <span class="section-label">Template vs. custom design</span>
    <p>A template-based site starts from a pre-built structure and costs less because much of the layout problem has already been solved. A custom design starts from your brand, your users, and your business goals. It takes longer, costs more, and tends to perform better in conversion because it was built for your specific audience rather than adapted from a generic one.</p>

    <span class="section-label">Who you hire</span>
    <p>In the Netherlands, web designers in Amsterdam earn an average of around €35,000 per year, putting typical hourly rates around €20–€45 for employed designers and higher for experienced freelancers. Agencies add coordination, quality assurance, and team depth, but also overhead. A senior freelance UX/UI designer in the Netherlands will typically charge between €65 and €120 per hour depending on experience and specialisation.</p>

    <span class="section-label">Strategy and UX thinking</span>
    <p>This is the variable most clients do not realise they are paying for, or not paying for. A website built without UX research, user journey mapping, or conversion strategy is cheaper upfront and more expensive over time. Research consistently shows that the cost of fixing UX problems after launch is roughly ten times higher than addressing them before or during the design phase. The strategic layer is not padding. It is the work that determines whether the finished site actually generates business.</p>

    <blockquote>
      "A website that costs €8,000 and converts 3% of its visitors is worth more than a €2,000 site that converts 0.5%. Focus on outcome, not invoice."
