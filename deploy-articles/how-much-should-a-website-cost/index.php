    <h2>The question you should actually be asking</h2>

    <p>Most people approach website pricing by asking: what is the cheapest I can get this for? The more useful question is: what does this website need to generate in leads or revenue to pay for itself, and what investment level makes that achievable?</p>

    <p>A €1,000 website that generates no leads is infinitely more expensive than a €3,500 website that brings in two new clients per month. The cost of a website is not what you pay for it. It is what it costs you when it does not work.</p>

