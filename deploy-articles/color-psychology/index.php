    <span class="section-label">Source 2 · University of Loyola, Maryland</span>

    <p>Research from the University of Loyola found that color increases brand recognition by up to 80%. This matters not just for recall; it matters for trust. Brands that use color consistently across their digital presence are recognized faster, and recognized brands are trusted more readily. The implication for website design is direct: a coherent, intentional color palette isn't a branding luxury. <strong>It's a conversion asset.</strong></p>

    <p>This research also helps explain why companies like Amazon, IKEA, and McDonald's have maintained strikingly consistent color systems for decades. These aren't accidents. They are high-value investments in visual memory.</p>

