    <h2>The Amazon Principle: Contrast Is the Real Driver</h2>

    <p>Amazon's "Add to Cart" button has become one of the most studied design elements in e-commerce. What's notable is that the color itself, a yellow-orange, is not inherently magical. What makes it work is <strong>contrast</strong>: a warm, saturated accent against a mostly white, neutral page. Amazon has maintained this approach for over two decades, iterating on saturation and shape while keeping the color logic intact.</p>

    <p>The principle that emerges is that <strong>no color works in isolation.</strong> The right color creates the clearest visual hierarchy on your specific page, guides the eye to the action you want taken, communicates the right emotion for your audience, and stays coherent with your broader brand identity.</p>

    <p>A/B testing consistently shows that a button color change alone rarely drives conversion. What drives conversion is when the color change also improves contrast, reduces visual noise, and aligns better with what the visitor expects from the brand. That's a UX problem, not just a color problem.</p>

