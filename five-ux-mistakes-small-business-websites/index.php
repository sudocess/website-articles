    <h1>The 5 UX mistakes most small business websites <em>are still making</em></h1>
    <p class="art-deck">Most small business websites lose visitors before anything meaningful happens. These five patterns show up again and again, and every one of them is fixable.</p>
    <p class="byline">By <strong>Engaging UX Design</strong> &nbsp;·&nbsp; UX Strategy &amp; Conversion</p>
  </div>

  <div class="art-body">

    <p>A website being live does not mean it is working. Research by GoodFirms found that 84.6% of web designers identify crowded design as the most common mistake on small business websites, followed by missing calls-to-action (38.5%) and hidden navigation (30.8%). These are not niche problems. They are the norm.</p>

    <p>What makes these mistakes costly is not that they are dramatic. Visitors do not usually close a tab in frustration and write an angry email. They simply leave, quietly, and find someone else. Here are the five patterns that cause that to happen most often.</p>

