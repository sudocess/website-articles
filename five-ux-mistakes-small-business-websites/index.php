    <h2>The five mistakes</h2>

    <ul class="mistake-list">

      <li>
        <div class="mistake-title">
          <span class="mistake-num">1</span>
          No clear call-to-action on the homepage
        </div>
        <p>Research shows that approximately 70% of small business websites do not have appropriate calls-to-action on their homepage. This is the single most damaging conversion mistake a site can make. A visitor who does not know what to do next does nothing. The fix is not just adding a button. It is deciding what the one most important action is for your business, making it visually prominent, and ensuring the language is specific ("Book a free call" beats "Contact us" every time).</p>
      </li>

      <li>
        <div class="mistake-title">
          <span class="mistake-num">2</span>
          Not optimised for mobile
        </div>
        <p>More than 62% of global web traffic now comes from mobile devices. Yet research from Baymard Institute finds that 81% of websites still perform poorly on mobile UX, and Statista data shows that when a mobile page takes more than three seconds to load, 53% of visitors leave. If your site was designed primarily for desktop and "adapted" for mobile as an afterthought, your mobile visitors are experiencing a fundamentally worse version of your business. That is a majority of your traffic.</p>
      </li>

      <li>
        <div class="mistake-title">
          <span class="mistake-num">3</span>
          Cluttered design with no visual hierarchy
        </div>
        <p>GoodFirms research identifies crowded web design as the most common design mistake, reported by 84.6% of professional web designers. Cluttered layouts overwhelm visitors and make it harder to identify what matters. Visual hierarchy, the deliberate arrangement of elements by size, weight, contrast, and position, is what guides a visitor's eye from awareness to action. Without it, every element competes equally for attention, and the visitor ends up paying attention to none of them.</p>
      </li>

      <li>
        <div class="mistake-title">
          <span class="mistake-num">4</span>
          Forms that ask for too much too soon
        </div>
        <p>Research by Nielsen Norman Group shows that reducing a form by even a single field can increase conversions by several percentage points. Yet most small business contact forms still ask for name, email, phone, company, budget, project description, and preferred callback time, all at the first point of contact. Every additional field is a small door that some percentage of visitors will not walk through. A simpler form converts more enquiries. Qualifying information can come later, once the relationship has started.</p>
      </li>

      <li>
        <div class="mistake-title">
          <span class="mistake-num">5</span>
          Slow page load times treated as a technical problem rather than a business problem
        </div>
        <p>A one-second delay in page load time leads to a 7% drop in conversions, according to research cited across multiple industry studies. At a business generating 50 enquiries a month, that is 3.5 enquiries disappearing silently every single month, every month, indefinitely. Page speed is frequently dismissed as something for developers to handle "at some point." But it is a direct revenue issue, and it is often fixable in a single session through image optimisation, script management, and caching configuration.</p>
      </li>

    </ul>

    <h2>The common thread</h2>

    <p>Every one of these mistakes has the same root cause: the website was built from the inside out, reflecting what the business owner wanted to say, rather than from the outside in, designed around how a visitor actually thinks and moves. UX design is the discipline that closes that gap. It is not about making things prettier. It is about removing every unnecessary barrier between a visitor and the action that grows your business.</p>

    <blockquote>
      "The most common website design mistakes are crowded web design (84.6%), no call-to-action (38.5%), and hidden navigation (30.8%)."
      <cite>GoodFirms Research, via emailvendorselection.com</cite>
    </blockquote>

    <p>The good news is that none of these require a full rebuild. In most cases, targeted fixes to an existing site, applied in order of impact, produce measurable results within weeks.</p>

