    <h2>What this delivered</h2>

    <p>The most visible outcome since launch has been Joey's reputation lift. His positioning was always strong, but distributed across a LinkedIn profile, a few essay drafts, and word of mouth. Concentrating it into a coherent, navigable narrative at a clean URL changed how peers and prospects perceive the practice. He's now sent the URL into rooms where previously he'd have sent a CV; that single substitution alone has reshaped some early conversations.</p>

    <p>The homepage's positioning line, <em>"Insight that moves decisions forward,"</em> together with the recurring framing that experience is a system rather than a program, has been picked up verbatim in multiple inbound conversations. The site is doing what a portfolio should: not just hosting credentials, but seeding the language a prospect uses when they describe their problem back to the practitioner.</p>

    <p>Inbound consultation bookings have increased meaningfully, and a notable share of them now arrive pre-qualified. Visitors are reading the essays, then booking. That changes the quality of the first call from <em>"tell me what you do"</em> to <em>"I think we should talk about X"</em>.</p>

    <h2>A few details worth highlighting</h2>

    <p>These are not the headlines, but they are the kind of detail that make a senior executive site land properly:</p>

    <ul class="detail-list">
      <li>
        <strong>Tone discipline.</strong>
        <p>Every page sounds like Joey, not like a copywriter. The voice has been kept consistent across reflections, case studies, and CTAs.</p>
      </li>
      <li>
        <strong>No carousel, no parallax, no scroll-jacking.</strong>
        <p>The decision was made early to let the content earn attention rather than tricks.</p>
      </li>
      <li>
        <strong>Contact gravity.</strong>
        <p>The "Book a call" action sits in the navigation, the hero, the about section, and the footer. Always one tap, but never shouting.</p>
      </li>
      <li>
        <strong>Bilingual SEO.</strong>
        <p>Every page exists in EN and NL with hreflang declarations, so each language is indexed separately and can rank in its own market.</p>
      </li>
      <li>
        <strong>Article surface.</strong>
        <p>Four case studies and reflections are presented as first-class content, not as blog footer-fodder. Each has its own page, schema markup, and shareable URL.</p>
      </li>
    </ul>

    <h2>What we'd take from this build</h2>

    <p>For senior practitioners selling expertise, the temptation is always to build a site that explains everything. The work here was largely about restraint: choosing what <em>not</em> to put on the page so that the things that remain carry weight. Three disciplines, four reflections, one CTA, two languages. That's the entire site architecture, and it has done more for the practice than a richer build would have.</p>

    <p>The other lesson worth keeping is that a clean URL substitution, sending a portfolio link into a room where you'd previously have sent a CV, quietly changes the kind of conversation you walk into. A portfolio doesn't have to convert in the marketing-funnel sense. Sometimes its job is to reshape the first sentence the other person says when the call begins.</p>

