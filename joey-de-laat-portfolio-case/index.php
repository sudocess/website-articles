    <h2>What was built</h2>

    <p>A focused executive portfolio designed to do one thing well: turn a 15-year career into a credibility surface that converts into 30-minute consultation bookings. Not a CV. Not a marketing site. A space that lets serious decision-makers, the kind who already know what governed insight systems are, immediately recognise that Joey is the kind of operator they want to talk to.</p>

    <p>The site is structured around three connected disciplines that frame his practice: governed insight systems, experience frameworks, and leadership enablement. Each is presented as a deliberate point of view, not a list of services. Four case studies and reflections live alongside the framing, with one essay, <em>CX as an operating system</em>, acting as the anchor piece a curious visitor can read end to end.</p>

    <p>The booking flow funnels every action toward a free 30-minute consultation. There is no contact form maze, no quote calculator, no service tiers. The single CTA is consistent across the page: book a call.</p>

    <h2>Design intent</h2>

    <p>The site reads as quiet confidence. Plenty of whitespace. Restrained typography: single-weight serif for headlines, sans for body, no decorative flourishes. Photography-grade portrait imagery rather than stock illustrations. The eyebrow-tagline-paragraph-quote rhythm of senior thought-leadership writing, not the bullet-point rhythm of marketing decks.</p>

    <p>The colour palette is deliberately corporate-adjacent without being sterile: deep navy primary, warm off-white background, a single accent for emphasis. Recognisable to a CFO, calming to a Chief Customer Officer, professional to a board director. The audience is operators who buy executive consulting, and the visual language has to read as such within the first three seconds.</p>

    <p>The bilingual structure (English default, Dutch mirror at <code>/nl/</code>) reflects Joey's reality. He operates internationally but is rooted in the Netherlands. The site doesn't auto-redirect; the visitor chooses, and the language switch persists across the journey.</p>

