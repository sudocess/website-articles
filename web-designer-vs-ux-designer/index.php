    <h1>What is the difference between a web designer <em>and a UX designer?</em></h1>
    <p class="art-deck">They both work on websites. They both use design tools. But they are solving fundamentally different problems. Understanding the distinction could save you from hiring the wrong person for your project.</p>
    <p class="byline">By <strong>Engaging UX Design</strong> &nbsp;·&nbsp; UX Strategy &amp; Conversion</p>
  </div>

  <div class="art-body">

    <p>Most small business owners use the terms interchangeably. "I need a web designer" often means "I need someone to build me a website." But the web design world has split into distinct specialisms, and hiring the wrong one for the job at hand is a common and costly mistake.</p>

    <p>Here is the clearest way to understand the difference: a web designer makes websites look good. A UX designer makes websites work well for the people using them. Both matter. But they are not the same skill set, and they are not the same type of work.</p>

    <h2>What a web designer does</h2>

    <p>A web designer is responsible for the visual layer of a website. That means layout, typography, colour palettes, image treatment, responsive behaviour across devices, and the overall aesthetic coherence of the site. They work on the front end — the part you can see — and often have skills in HTML, CSS, and sometimes JavaScript.</p>

    <p>Their primary question is: does this look right? Does it represent the brand? Is it visually appealing and technically functional across devices? Web designers are, in the words of one common analogy, like architects: they design the structure and appearance of the building.</p>

    <h2>What a UX designer does</h2>

    <p>A UX (user experience) designer is responsible for how a website feels to use. They research users, map user journeys, define information architecture, design wireframes that reflect how people actually think and navigate, and conduct usability testing to find friction points.</p>

    <p>Their primary question is: does this work for the person using it? Does the structure make sense? Can someone find what they need without thinking too hard? Does the flow from landing on the page to taking an action feel natural? UX designers are the interior designers in the same analogy: focused on how people move through the space and whether each room serves its purpose.</p>

