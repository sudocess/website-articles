    <h2>Where it gets complicated</h2>

    <p>In larger organisations, these roles are clearly separated. A UX designer does the research and wireframing. A UI designer handles the visual interface. A web designer or developer handles the build. Each is a specialist.</p>

    <p>For small businesses and freelance work, the reality is different. Many designers cover multiple areas. A freelancer who describes themselves as a "UX/UI designer and web developer" is someone who handles the full stack — research, design, and build — in a single engagement. This is not a compromise. For most small business projects, it is exactly what you need: one person who understands both how the site should work and how it should look, and who can build it.</p>

    <blockquote>
      "Web design focuses on creating websites that work. UX design focuses on the user. The UX designer considers the best way to optimise a product to cater to the user's specific needs."
      <cite>Userpilot, Web Designer vs UX Designer, 2026</cite>
    </blockquote>

    <h2>Which one do you actually need?</h2>

    <p>If you need a website built and your primary concern is that it looks professional and functions correctly, a web designer covers you. If you are rebuilding an existing site because it is not converting, losing visitors, or generating complaints about usability, you need UX work before visual work. Redesigning the visuals of a site that has structural UX problems does not fix the conversion rate. It just makes the same problems look better.</p>

    <p>The most valuable hire for most small businesses is someone who thinks like a UX designer (starting with user needs and business goals) and executes like a web designer (building something that works, looks great, and goes live). That overlap is exactly where conversion-focused freelancers operate.</p>

